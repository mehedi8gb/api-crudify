        $this->newLine();
        $this->line('  ' . str_repeat('‚ĒÄ', 60));
        $this->line('  <fg=green;options=bold>‚úď Installation complete!</>');
        $this->newLine();
    }

    private function addHelpersAutoload(): void
    {
        $composerPath = base_path('composer.json');
        $composer     = json_decode(file_get_contents($composerPath), true);
        $helpersFile  = 'app/Helpers/Helpers.php';

        $this->newLine();

        if (in_array($helpersFile, $composer['autoload']['files'] ?? [])) {
            $this->line('  <fg=yellow>‚ö†  Helpers.php already registered in composer.json</>');
        } else {
            $composer['autoload']['files'][] = $helpersFile;
            file_put_contents(
                $composerPath,
                json_encode($composer, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES) . PHP_EOL
            );
            $this->line('  <fg=green>‚úď composer.json autoload updated</>');
        }

        $this->newLine();
        $this->runComposerDumpAutoload();
    }

    private function runComposerDumpAutoload(): void
    {
        if (function_exists('generateUniqueNumber')) {
            $this->line('  <fg=green>‚úď Helpers already autoloaded ‚ÄĒ dump-autoload not needed</>');
            return;
        }

        $this->line('  <fg=blue>‚ÜĽ Running composer dump-autoload...</>');

        $process = new Process(['composer', 'dump-autoload']);
        $process->setWorkingDirectory(base_path());
        $process->run();

        if ($process->isSuccessful()) {
            $this->line('  <fg=green>‚úď composer dump-autoload completed</>');
        } else {
            $this->line('  <fg=yellow>‚ö†  Could not run composer automatically.</>');
            $this->line('  <fg=yellow>   Run manually: composer dump-autoload</>');
        }
    }

